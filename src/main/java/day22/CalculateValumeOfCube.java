package day22;

public class CalculateValumeOfCube {

    public double volumeOfCube(double a) {
        return a * a * a;
    }


    public static double volumeOfSquarePrism(double a,double b){
        return a*a*b;

    }
    public static double volumeOfRectangularPrism(double a,double b,double c){
        return a*b*c;

    }



}